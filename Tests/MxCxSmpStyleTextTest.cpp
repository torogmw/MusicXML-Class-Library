	std::string expected;
	std::string actual;
	object.setDefaultX( MxNumberTenths( 283.7 ) );
	object.setDefaultY( MxNumberTenths( 283.7 ) );
	object.setRelativeX( MxNumberTenths( 283.7 ) );
	object.setRelativeY( MxNumberTenths( 283.7 ) );
